		if(diff < 480){
			diff = 480;
		}

		if(diff > 955){
			diff = 955;
		}


		// ipad landscape window.innerHeight === 704;
		// innerHeight - menu 90 === 614;

		if(diff < 614){
			// alert(window.innerHeight);
			$('.section').each(function(i,n){
				$(n).toggleClass("section");
			});
		}

		return {"height" : diff, "diff" : menu, "sectionHeight" : sectionHeight, "width" : width, "actualHeight": actualHeight, "scrolling" : arr};
	}

